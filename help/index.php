          <h3>What is Watch Folders? How do I set it up?</h3>
          <div>
            <p>Watch folders allow you to monitor the content of the specific directory. It will automatically add/remove tracks in your Songbird library, depending on the changes you perform in that certain directory.
            <br>You can set it up on the first run or while running the app:
            <br>1.  Go to Tools -> Options (Songbird -› Preferences on a Mac)
            <br>2.  Select the Media Importer tab -> Watch Folders.
            <br>3.  Choose which folder you want to watch for changes.
            </p>
