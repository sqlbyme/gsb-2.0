          <h3>I love my Songbird library and prefernces, and I would like to use it on another machine. Is that possible?</h3>
          <div>
            <p>Yes! But it will only work if you copy your profile to machine with the same Operating System. Here's how to do it: You need to capture your Songbird profile first (this is where all your preferences, artwork, cache).
            <br>On Windows profile artwork and cache are stored in AppData\Local\Songbird2. Crash reports, add-ons, library db files, preferences and all kinds of other important files are stored in AppData\Roaming\Songbird2. Basically you need to capture both and remember which is which.
            <br>On Mac machines Songbird profile is ~/Library/Application Support/Songbird2/.
            <br>Once you captured those, transfer them to another machine with the same OS into corresponding directories. Make sure to keep the same string of numbers and letters (/********.default/).  Songbird won't recognize your library correctly if you modify the profile name.  Now open 'profiles.ini', located in the root Songbird2 folder, and adjust the "Path" field to read the string of letters and numbers that your profile is.
            </p>
          </div>
