          <h3>What is DLNA?</h3>
          <div>
            <p>DLNA stands for Digital Living Network Alliance, a collective trade organization started by Sony that is responsible for designing interoperability guidelines to enable sharing of digital media across multimedia devices. After that explanation, things get pretty technical. If you’re really curious, visit their Web Site at: http://www.dlna.org/</p>
          </div>
          <br>
          <h3>How to enable the Songbird DLNA Sharing Add-On:</h3>
          <div>
            <p>When Songbird is first installed on your computer, the DLNA Sharing Add-On is enabled by default.  If you wish to use the Sharing Add-On, then you’re all set to go. If do not wish to use it, simply click on the “Media Server” icon/text in the lower right hand corner of the main Songbird Desktop window and turn it off. Just know you can turn it on at anytime to connect your living room to Songbird for a media rich experience. Try it!</p>
          </div>
          <br>
          <h3>Library Scanning:</h3>
          <div>
            <p>When you add media to your Songbird library, the files need to be scanned by the Sharing Add-On so your DLNA compatible device will recognize them. If you have lots of cool tunes and videos, it can take a little while to scan them all.  Once scanning is completed, you should be good to go.  It’s best to just let Songbird do its thing while scanning so that all files get scanned in a single pass. If you have thousands of files, and we know some of you do, it’s going to take awhile.</p>
          </div>
          <br>
          <h3>Why doesn’t my device recognize my files?</h3>
          <div>
            <p>Not all DLNA enabled devices support all types of media file formats. You should refer to your device(s) user guide first to find out the correct media formats they support, and make sure all your files are in that compatible format for a smooth and successful streaming experience. If you files are not compatible, try to find a media utility on the Internet you can use to convert your files to the right formats your device will recognize.</p>
          </div>
          <br>
          <h3>What is the difference between DLNA “Push” and “Pull” Control:</h3>
          <div>
            <p>“Push” control means you select the media you wish view on your DLNA enabled device in the main song bird library and drag it into the right hand Push Controller window pane after selecting your device from the device pull down menu. Then, once you’ve dragged over the music and/or video you want to check out, click the play button and that’s it.

            “Pull” control means you use the networking function on your DLNA enabled device to discover the Songbird library being shared when the Sharing Add On is running, and simply navigate to what you want to play. Simple. If you haven’t done that before and don’t know how, you better dig out the user manual for your device. If you lost the manual, I bet you can find it on the internet.</p>
          </div>
          <br>
          <h3>Can I play a playlist with my DLNA Device?</h3>
          <div>
            <p>Yup. Just create the playlist in the main Songbird library first, and then either “pull” it over from your device, or to “push” it, just drag and drop it into the selected device in the right hand push controller window pane after you’ve selected it from the device pull down and click that large “Play” button.</p>
          </div>
          <br>
          <h3>Sometimes my files are grayed out in the DLNA push controller play queue, why?</h3>
          <div>
            <p>Songbird tries to filter out files that your device can’t play. We based that upon the information gathered during the scanning process. If you have a favorite music track or video file you can’t play with your device, you’ll most likely have to convert it into a compatible file format your DLNA enabled device supports. Again, refer to your device’s user manual for a list of supported formats and find a file conversion utility on the internet and convert it. They, try again. It can be a complicated process, but we know you can do it. Good luck!</p>
          </div>
