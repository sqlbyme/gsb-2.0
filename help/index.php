          <h3>What audio and video formats does Songbird support?</h3>
          <div>
            <p>Songbird is playing most audio formats such as MP3, AAC, ALAC, FLAC, Vorbis, WMA, WAV, AIFF.
              WMA with Windows Media on Windows. AAC with FairPlay on Mac.
              Oh, and did you know about the video, too?
              All platforms: Ogg Theora video.
              MacOS: MPEG 4 and H.264 video, in MP4 files.
              Windows: WMV, and if you have QuickTime installed, we also support MPEG-4 and H.264 in MP4.
