          <h3>I updated Songbird and some of my favorite addons don't work any more. How can i have them work again?</h3>
          <div>
            <p>I assume those are community addons which were created by our great generous community supporters. And some of them are not updated to be compatible with the current Songbird version. We know that you love those unsupported addons, and we came up with a little trick on how to help you make them work again.
            <br>- Open a new tab and enter about:config in the address bar.
            <br>- Agree to the "I'll be careful!" warning.
            <br>- Right-click anywhere in the list, and select the "New Boolean" option.
            <br>- Enter extensions.checkCompatibility.2.2, as the name (or Whatever Songbird version you have: 2.1, 2.0) and set the value to False.
            <br>- Restart Songbird.</p>
