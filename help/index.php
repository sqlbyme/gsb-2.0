            <br>Win32: Start > Songbird > Songbird (Profile Manager)
            <br>MacOSX: Open Terminal (or other command-line application) and run the following commands, pressing return after each line :
            <br>• cd /Applications/Songbird.app/Contents/MacOS/
            <br>./songbird -p
