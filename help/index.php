          <h3>Creating new profile didn't help, I would like to reinstall Songbird.</h3>
          <div>
            <p>Your app has been acting strangely, new profile didn't help it, and you decided to reinstall?
            <br>1.  Delete your profile via the Profile Manager, being sure to select the "Delete files" button.
            <br>2.  Uninstall Songbird using the Uninstaller.
            <br>3.  Delete C:/Users/YOUR_USER_NAME/AppData/Local/Songbird2 and C:/Users/YOUR_USER_NAME/AppData/Roaming/Songbird2 . Delete everything out of C:/Users/YOUR_USER_NAME/AppData/Local/Temp and C:/Temp and C:/Windows/Temp .
            <br>4.  If you're still having issues, you can also go into regedit, search for Songbird registry key anf remove it (Software/Songbird) .
            <br>For Mac users: delete Songbird from your Applications directory.
            <br>And then install a preferred Songbird build :)
            </p>
