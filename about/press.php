					<p>“I have been waiting for a music discovery app like this. My friends and I are onto the latest and greatest music, and I love the great finds I make in Songbird. It sucks that I have to cut and paste YouTube URLs to share the music I love… Songbird allows me to share what I am passionate about with my closest friends without ever leaving my music. This is an awesome app.”</p> 
