               <br/>

					<p><strong>Songbird is ranked in the top 1% of apps</strong> in the Google and Amazon stores. In addition to the Android mobile phone version, the new Songbird apps, with the effortless discovery feature, are available on Mac, Windows and the Web with Facebook Connect. An iOS version is coming soon.</p>

                <br/>

					<p><strong>“Lead the Flock” Contest</strong><br/>
					In association with its product debut, and in the spirit of sharing, Songbird has launched “Lead the Flock”, a social media-based contest that rewards users with valuable music-related prizes for sharing meaningful music experiences with the community, downloading and using Songbird, and for referring Songbird to their friends. The contest begins today and will only last one month. Winners will be announced on July 12. Contest details can also be found at <a href="http://getsongbird.com/promotions/leadtheflock/enter/" class="pink" target="_blank">getsongbird.com</a></p>


