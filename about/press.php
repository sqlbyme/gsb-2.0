        	<h1>Songbird® Announces Ultimate Music Discovery Apps</h1>
            
            <div class="press_item">
            	<p><i>Songbird Resolves the Failure of Spotify, Pandora and iTunes to provide an Effortless, Personal, Social Way to Discover New Music</i></p>

					<p><i>Launches social music discovery contest;  winners to receive $1500 for live shows & concerts.</i></p>

					<p>June 12, 2012; San Francisco, CA — Music lovers today are being cheated by music apps with limited social and discovery features. Representing an entirely new approach to music discovery, Songbird today launched their new apps for Android and other platforms that make the music discovery experience fundamentally social, provide personalized recommendations and deliver music beyond the tracks.</p>

					<p>Songbird’s research reveals deep music fan dissatisfaction with arcane methods of discovery like cutting and pasting URLs, sharing playlists and broadcasting Top 50 charts. Apps and services like Spotify, Pandora and iTunes have relegated discovery to endless searches; wading through news feed posts, visiting dozens of web sites or repeatedly listening to the same 30 music tracks on a radio station. In particular, fans noted that:</p>

               <ul id="bullets">
					<li>discovering new music and artists is difficult and time-consuming. A May 2012 research study by Songbird revealed that more than 93% of music lovers are constantly on the lookout for new artists and new music; however, those music lovers said that the search process is often fruitless and very difficult. </li>

					<li>yet “effortless music discovery” rated between “important” and “absolutely vital” for a digital music service.</li></ul>
					 
					<p>“Songbird is exploding traditional notions of music discovery. Our goal is to remove all the barriers to connecting people to artists, the music they love and their friends. The days of stale playlists, boring genre replays, pay-for-play recommendations and track-only music experiences are over!” said Les Schmidt, Songbird CEO. </p>

                <p>The new Songbird apps uniquely empower music fans with a music discovery experience that is:</p>
 
                <ul id="bullets">

					<li><strong>EFFORTLESS. New music, new artists</strong> and fresh music updates are delivered straight to you…in the apps fans already use. Songbird’s first-of-its-kind “effortless discovery” feature suggests new artists and new music to fans based solely on the artists and music the fan already loves. Fans can follow an artists’ every move, share anything Songbird delivers with friends and even purchase music from 7digital, the largest licensed distributor of MP3 tracks in the world - effortlessly. </li>

					<li><strong>UP CLOSE. Stay current with favorite artists;</strong> get their updates real-time about concerts, videos and any images they post. With Songbird, music fans can listen to their music and “dive deep” in the same apps to learn the latest about artists, drawing from sources such as Facebook posts, YouTube videos, SoundCloud audio tracks, Wikipedia and last.fm bio information, and Flickr images. Videos, concert news, photos, and more all get delivered into a single music experience saving fans tons of time and simultaneously enabling a much richer music experience.</li>

					<li><strong>PERSONAL.</strong> Songbird is all about the individual music fan. New artists are suggested based on the music and artists a fan already knows and loves, not just what everyone else is already listening to. Only updates about artists you care about are delivered directly to you! We use the power of the internet to deliver an experience that is unique to each and every fan.</li>

					<li><strong>SOCIAL.</strong> Songbird’s new Facebook-connected apps gives easy insight into friends’ music interests too. Discover new artists directly from friends by easily seeing which artists they like or follow. That new discovery you found in your friends’ interests can buy easily purchased or shared with anyone – all with one click, all in the same app! Connect with friends in ways never before possible.</li></ul>

